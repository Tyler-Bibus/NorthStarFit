package com.northstarfit.ui.theme

import androidx.compose.ui.unit.dp

/**
 * Centralized spacing scale so every screen breathes the same way.
 * Use these instead of ad-hoc dp literals in layout padding/arrangement.
 */
object Spacing {
    val xs = 4.dp
    val sm = 8.dp
    val md = 12.dp
    val lg = 16.dp
    val xl = 24.dp
    val xxl = 32.dp
}
